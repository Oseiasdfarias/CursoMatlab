clc;
clear all;
close all;

a = 45;
b = 90;
c = 180;

%% Seno e cosseno em Radianos

% Transformando ângulos de Graus para Radiano

ar = (a * pi) / 180;
br = (b * pi) / 180;
cr = (c * pi) / 180;

sin(ar)
sin(br)
sin(cr)

cos(ar)
cos(br)
cos(cr)

%% Seno dos ângulos em Graus

sind(a)
sind(b)
sind(c)

% Cosseno dos ângulos em Graus

cosd(a)
cosd(b)
cosd(c)
